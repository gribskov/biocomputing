import sys
import pysam

# --------------------------------------------------------------------------------------------------\
# main program
# --------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    bamfile = sys.argv[1]
    sys.stderr.write('bam file: {}'.format(bamfile))

    bam = pysam.AlignmentFile(bamfile, 'rb')
    nbam = 0
    for read in bam:
        nbam += 1
        print(read)
        if nbam > 4:
            break

exit(0)
