def group(arr, idx):
    group = {}
    for row in arr:
        id = tuple(row[x] for x in idx)
        if id in group:
            i = 0
            for x in range(len(row)):
                if x not in idx:
                    group[id][i] += row[x]
                    i += 1
        else:
            group[id] = [row[x] for x in range(len(row)) if x not in idx]

    return group


print(group([[1, 2, 3], [1, 5, 6], [1, 8, 9]], [0]))
