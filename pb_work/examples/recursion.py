def factorial(n):
    """---------------------------------------------------------------------------------------------
    recursive factorial function
    factorial(n) = n * factorial(n-1)
    ---------------------------------------------------------------------------------------------"""
    if n <= 1:
        return 1;

    f = n * factorial(n - 1)
    return f

def factorialStack(n):
    """----------------------------------------------------------------------------------
    factorial using stack
    ----------------------------------------------------------------------------------"""
    # create stack
    stack = []
    while n > 0:
        stack.append(n)
        n -= 1

    # multiply all stack values
    f = 1
    while stack:
        f *= stack.pop()

    return f


for i in range(6):
    print(i, factorial(i))
    print(i, factorialStack(i))
