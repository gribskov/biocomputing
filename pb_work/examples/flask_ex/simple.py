from flask import Flask
app = Flask(__name__)

@app.route('/<worldtype>')
def welcome(worldtype):
   return '<H1>Welcome to the {} world </H1>'.format(worldtype)

# --------------------------------------------------------------------------------------------------
#
# --------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    app.debug = True
    app.run()

    exit(0)
