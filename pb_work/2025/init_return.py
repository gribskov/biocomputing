class Test:

    def __init__(self):
        self.a = 1
        return None


# --------------------------------------------------------------------------------------------------
#
# --------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    this = Test()
    print(f'a:{this.a}')
    exit(0)
